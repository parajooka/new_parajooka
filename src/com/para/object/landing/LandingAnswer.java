package com.para.object.landing;

public class LandingAnswer {
	private int answer_id;
	private int question_id;
	private int pring_seq;
	private String answer_title;
	private String answer_img;
	private String event_function;
	private String event_img;
	
	public int getAnswer_id() {
		return answer_id;
	}
	public void setAnswer_id(int answer_id) {
		this.answer_id = answer_id;
	}
	public int getQuestion_id() {
		return question_id;
	}
	public void setQuestion_id(int question_id) {
		this.question_id = question_id;
	}
	public int getPring_seq() {
		return pring_seq;
	}
	public void setPring_seq(int pring_seq) {
		this.pring_seq = pring_seq;
	}
	public String getAnswer_title() {
		return answer_title;
	}
	public void setAnswer_title(String answer_title) {
		this.answer_title = answer_title;
	}
	public String getAnswer_img() {
		return answer_img;
	}
	public void setAnswer_img(String answer_img) {
		this.answer_img = answer_img;
	}
	public String getEvent_function() {
		return event_function;
	}
	public void setEvent_function(String event_function) {
		this.event_function = event_function;
	}
	public String getEvent_img() {
		return event_img;
	}
	public void setEvent_img(String event_img) {
		this.event_img = event_img;
	}
}
